                    <p>
                        <h3>I. Ease of Use (Can a child can use it with minimal help?)</h3>
                        Note that this factor is combined with “Childproof” on the short form of this instrument.<br/>
                        N SE A NA<br/>
                        __ __ __ __ Skills needed to operate the program are in range of the child<br/>
                        __ __ __ __ Children can use the program independently after the first use<br/>
                        __ __ __ __ Accessing key menus is straightforward<br/>
                        __ __ __ __ Reading ability is not prerequisite to using the program<br/>
                        __ __ __ __ Graphics make sense to the intended user<br/>
                        __ __ __ __ Printing routines are simple<br/>
                        __ __ __ __ It is easy to get into or out of any activity at any point<br/>
                        __ __ __ __ Getting to the first menu is quick and easy<br/>
                        __ __ __ __ Controls are responsive to the touch<br/>
                        __ __ __ __ Written materials are helpful<br/>
                        __ __ __ __ Instructions can be reviewed on the screen, if necessary<br/>
                        __ __ __ __ Children know if they make a mistake<br/>
                        __ __ __ __ Icons are large and easy to select with a moving cursor<br/>
                        __ __ __ __ Installation procedure is straightforward and easy to do
                    </p>
                    <p>
                        <h3>II. Childproof (Is it designed with “child-reality” in mind?)</h3>
                        Note that this factor is combined with I. in the short form of this instrument.<br/>
                        N SE A NA<br/>
                        __ __ __ __ Survives the “pound on the keyboard” test; more recently, the digital playdoh test<br/>
                        __ __ __ __ Offers quick, clear, obvious response to a child's action<br/>
                        __ __ __ __ The child has control over the rate of display<br/>
                        __ __ __ __ The child has control over exiting at any time<br/>
                        __ __ __ __ The child has control over the order of the display<br/>
                        __ __ __ __ Title screen sequence is brief or can be bypassed<br/>
                        __ __ __ __ When a child holds a key down, only one input is sent to the computer<br/>
                        __ __ __ __ Files not intended for children are safe<br/>
                        __ __ __ __ Children know when they’ve made a mistake<br/>
                        __ __ __ __ This program would operate smoothly in a home or classroom setting
                    </p>
                    <p>
                        <h3>III. Educational</h3>
                        What can a child learn from this program? What do they walk away from the experience with, that they didn’t have when they first came to the experience?<br/>
                        N SE A NA<br/>
