                        __ __ __ __ Graphics do not detract from the program’s educational intentions<br/>
                        __ __ __ __ Feedback employs meaningful graphic and sound capabilities<br/>
                        __ __ __ __ Speech is used<br/>
                        __ __ __ __ The presentation is novel with each use<br/>
                        __ __ __ __ Good challenge range (this program will grow with the child)<br/>
                        __ __ __ __ Feedback reinforces content (embedded reinforcements are used)<br/>
                        __ __ __ __ Program elements match direct experiences<br/>
                        __ __ __ __ Content is free from gender bias<br/>
                        __ __ __ __ Content is free from ethnic bias<br/>
                        __ __ __ __ A child’s ideas can be incorporated into the program<br/>
                        __ __ __ __ The program comes with strategies to extend the learning<br/>
                        __ __ __ __ There is a sufficient amount of content
                    </p>
